    public void dataPanel(Object obj)
    {
        cvaTextField.setText(Integer.toString(((Institucion) obj).getCveIntitucion()));
        nomTextField.setText(((Institucion) obj).getNombre());
        dirTextField.setText(((Institucion) obj).getDir());
        telTextField.setText(Long.toString(((Institucion) obj).getTel()));
    }

