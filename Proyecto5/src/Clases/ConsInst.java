    public String desp0(Nodo raiz, int nivel)
    {
        String s = "";
        if (raiz != null)
        {
            s = raiz.getEtiqueta();
            insComboBox.addItem(s);
            desp0(raiz.getSig(), 0);
        }
        return s;
    }

    public void actualizarUI()
    {
        GuardaArchivos.guardar(Principal.raiz, "Multilista.dat");
        desp0(Principal.raiz, 0);
    }

    public void dataPanel(Object obj)
    {
        cvaTextField.setText(Integer.toString(((Institucion) obj).getCveIntitucion()));
        nomTextField.setText(((Institucion) obj).getNombre());
        dirTextField.setText(((Institucion) obj).getDir());
        telTextField.setText(Integer.toString(((Institucion) obj).getTel()));
    }

